class TestExample:
    def test_check_math_true(self):
        a = 5
        b = 9
        expected_sum = 14
        assert a + b == expected_sum, f"Ошибка - число не равно a + b, должно быть {expected_sum}"
    def test_check_math_fail(self):
        a = 5
        b = 400
        expected_sum = 14 # выносим сумму отдельно, чтобы не дублировать ниже 2 раза одно и тоже значение
        assert a+b == expected_sum, f"Ошибка - число не равно a + b, должно быть {expected_sum}"
