# запускаем тест 'python -m pytest test_examples.py -k "test_check_math"'. Конструкцию -k "test_check_math" можно убрать чтобы запустить все тесты
# код выше означает "питон, запусти модуль пайтест в файле тест_экзампл.пай, а именно функцию под названием тест_чек_матх
# assert - это инструкция в Python, она проверяет, является ли выражение истинным и если нет - возвращает Ошибку
# функция всегда должная начинаться с test_, иначе pytest будет считать эту функцию вспомогательной и не будет ее запускать с другими тестами
# У нас есть 2 теста, оба начинаются с test_check_math, так вот, если мы в консоли указываем часть названия теста (с начала), то он запускается. Думаю, это сделано для того, что бы запускать тесты кластерами, там самым облегчая группировку по названию.